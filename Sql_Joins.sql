create database JOINDEMO;
USE JOINDEMO;

-- INNER JOIN

CREATE TABLE EMP(
EMPID INT PRIMARY KEY,
FNAME VARCHAR(50),
AGE INT 
);

CREATE TABLE SALARY(
SAL_ID INT PRIMARY KEY,
EMPID INT,
AMOUNT DECIMAL(10,2)
);

INSERT INTO EMP VALUES
(1,'A',24),(2,'B',25),(3,'C',26);

INSERT INTO SALARY VALUES
(101,1,50000),(102,2,55000);

SELECT E.EMPID,FNAME,S.AMOUNT
FROM EMP E
INNER JOIN SALARY S
ON E.EMPID=S.EMPID;

