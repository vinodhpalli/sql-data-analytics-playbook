-- CURDATE() -- RETURNS CURRENT DATE(YYYY-MM-DD) FORMAT

SELECT CURDATE() AS TODAYDATE;
SELECT CURRENT_DATE() AS TODAYDATE;

-- CURTIME() -- RETURNS CURRENT TIME(HH:mm:ss) FORMATE

SELECT CURTIME() AS CURRENTTIME;
SELECT CURRENT_TIME() AS CURRENTTIME;

-- NOW() -- RETURNS CURRENT DATE AND TIME
SELECT NOW() AS CURRENTDATETIME;

-- SYSDATE() --RETURNS SYSTEMS CURRENT DATE AND TIME
SELECT SYSDATE() AS SYSTEMDATE;

-- DATE() -- EXTRACTS DATE PART FROM THE DATE TIME VALUE

SELECT DATE('2025-02-07 08:04:30') AS ONLYDATE;

-- TIME() -- EXTRACTS TIME PART FROM THE DATE TIME VALUE

SELECT TIME('2025-02-07 08:04:30') AS ONLYTIME;

-- YEAR(),DATE(),DAY()
SELECT
YEAR('2025-02-07') AS YEARPERT,
MONTH('2025-02-07') AS MONTHPART,
DAY('2025-02-07')AS DAYPART;

-- DAYNAME()-- RETURNS WEEK DAYNAME

SELECT DAYNAME('2025-02-07') AS DAYNAME;

SELECT DAYNAME('2025-14-07') AS DAYNAME;

SELECT DAYNAME('2025-02-33') AS DAYNAME;

-- MONTHNAME() -- RETURNS MONTH NAME

SELECT MONTHNAME('2025-02-07') AS MONTHNAME;

-- DATE_FORMAT()
-- %Y=YEAR(4 DIGITS)
-- %m=MONTH(2 DIGITS)
-- %M=MONTH NAME
-- %d= DAY
-- %W= WEEKDAYS NAME
-- %H=HOURS
-- %i= MINUTES
-- %s=SECONDS

SELECT DATE_FORMAT('2026-03-31' ,'%d-%M-%Y-%W') AS FORMATEEDDATE;

-- DATEDIFF()
SELECT DATEDIFF('2025-12-30','2025-12-10') AS DATEDIFFERENCE;
SELECT AVG(DATEDIFF('2025-12-30','2025-12-10')) AS DATEDIFFERENCE;

-- ADDDATE()/DATE_ADD()

SELECT ADDDATE('2025-12-30', INTERVAL  1 YEAR) AS NEWDATE;
SELECT ADDDATE('2025-12-30', INTERVAL  2 MONTH) AS NEWDATE;
SELECT ADDDATE('2025-12-30', INTERVAL  10 DAY) AS NEWDATE;

-- SUBDATE()/DATE_SUB
SELECT SUBDATE('2025-12-30', INTERVAL  10 DAY) AS NEWDATE;
SELECT SUBDATE('2025-12-30', INTERVAL  1 YEAR) AS NEWDATE;
SELECT SUBDATE('2025-12-30', INTERVAL  1 MONTH) AS NEWDATE;

-- ADDTIME() -- ADD A TIME INTERVEL TO A GIVEN TIME OR DATETIME

SELECT ADDTIME('2025-12-30 03:30:00', '02:15:00') AS NEWTIME;

-- SUBTIME()/TIME_SUB()
SELECT SUBTIME('2025-12-30 03:30:00', '02:15:00') AS NEWTIME;

-- TIMEDIFF() -- RETURNS THE DIFFERNCE BETWEEN TWO DATETIME OR TWO TIME VALUES
SELECT TIMEDIFF('10:30:00','08:00:00') AS TIMEDIFFERENCE;

-- EXTRACT() -- EXTRACTS SPECIFIC PART FORM DATE

SELECT EXTRACT(YEAR FROM '2025-11-07') AS EXTRACTEDYEAR;
SELECT EXTRACT(DAY FROM '2025-11-07') AS EXTRACTEDYEAR;
SELECT EXTRACT(MONTH FROM '2025-11-07') AS EXTRACTEDYEAR;





