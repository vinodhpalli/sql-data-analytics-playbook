CREATE DATABASE SUBQUERY;
USE SUBQUERY;

CREATE TABLE EMPLOYEES(
EMPID INT PRIMARY KEY,
EMPNAME VARCHAR(50),
DEPARTMENT VARCHAR(50),
SALARY INT 
);

