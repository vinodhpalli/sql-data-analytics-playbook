CREATE DATABASE BANKDB;
USE BANKDB;

CREATE TABLE ACCOUNTS(
ACCOUNT_ID INT PRIMARY KEY,
ACCOUNT_HOLDER VARCHAR(100),
BALANCE DECIMAL(10,2) CHECK (BALANCE >=0)
);

