CREATE DATABASE STUDENTDB;
USE STUDENTDB;

CREATE TABLE STUDENT(
STUDENTID CHAR(4),
NAME VARCHAR(50),
MARKS int
);

INSERT INTO STUDENT VALUES
('S001','RAM',90),
('S002','RAVI',80),
('S003','KARAN',70),
('S004','MEENA',60),
('S005','RIYA',50),
('S006','PRIYA',40);

-- SIMPLE CASE STATEMENT

SELECT 
STUDENTID,NAME,MARKS,
CASE MARKS
WHEN 90 THEN "OUTSTANDING"
WHEN 80 THEN "EXCELLENT"
WHEN 70 THEN "VERYGOOD"
WHEN 60 THEN "GOOD"
WHEN 50 THEN "PASS"
ELSE "FAIL"
END AS REMARK
FROM STUDENT;

