CASE
WHEN MARKS BETWEEN 90 AND 100 THEN "OUTSTANDING"
WHEN MARKS BETWEEN 80 AND 89 THEN "EXCELLENT"
WHEN MARKS BETWEEN 70 AND 79 THEN "VERYGOOD"
WHEN MARKS BETWEEN 60 AND 69 THEN "GOOD"
