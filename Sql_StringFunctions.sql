CREATE DATABASE STRDB;
USE STRDB;

CREATE TABLE EMPLOYEES(
EMPID INT,
FIRSTNAME VARCHAR(30),
LASTNAME VARCHAR(30),
DEPARTMENT VARCHAR(30),
CITY VARCHAR(30)
);

